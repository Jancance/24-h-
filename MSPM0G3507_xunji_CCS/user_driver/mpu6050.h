#ifndef MPU6050_H
#define MPU6050_H

#include <stdint.h>

/*
 * MPU6050 服务目标一和目标二的直线段：
 *   读陀螺仪 Z 轴角速度，积分得到 yaw 角，用来修正车头方向。
 *
 * 注意：
 *   1. MPU6050 的 yaw 是陀螺仪积分出来的，短时间直行够用，时间长会漂。
 *   2. 每段直线开始前都会清零 yaw，减小长时间积分漂移。
 *   3. 上电校准时车必须静止，否则零偏会算错。
 */

uint8_t mpu6050_init(void);

/*
 * 校准 Z 轴陀螺仪零偏。
 *
 * 为什么要校准：
 *   MPU6050 静止时，陀螺仪理论上应该读 0 deg/s。
 *   但实际会有一个小偏差，比如静止也读到 0.5 deg/s。
 *   如果不减掉这个偏差，yaw 积分会一直慢慢漂。
 *
 * sample_count：
 *   采样次数。次数越多越稳，但上电等待时间越长。
 *
 * 返回：
 *   1 = 校准成功
 *   0 = I2C 读取失败
 */
uint8_t mpu6050_calibrate_gyro_z(uint16_t sample_count);

/*
 * 更新 yaw 角。
 *
 * dt_s：
 *   两次调用之间的时间，单位秒。
 *   直线控制循环是 10ms，所以传 0.01f。
 *
 * 内部做的事：
 *   读取 Z 轴角速度 -> 减零偏 -> 换算成 deg/s -> 积分成 yaw 角。
 */
/*
 * 当前实现里还做了两个抗干扰处理：
 *   1. 一阶低通滤波：减小电机震动、地面颠簸造成的角速度毛刺。
 *   2. 死区处理：角速度特别小时当成 0，减少静止时 yaw 慢慢漂。
 */
uint8_t mpu6050_update_yaw(float dt_s);

/* 把当前 yaw 角清零，通常在开始直行前调用。 */
void mpu6050_reset_yaw(void);

/*
 * 获取当前 yaw 角，单位是度。
 * 每段直线都把该段开始时的车头方向当作 0 度。
 */
float mpu6050_get_yaw_deg(void);

/* 获取当前 Z 轴角速度，单位 deg/s，主要用于调试观察。 */
float mpu6050_get_gyro_z_dps(void);

#endif /* MPU6050_H */
